// Scan public/photos/{CODE}/ directories and generate src/data/photos.js
// Usage: node scripts/generate-photos-js.mjs

import { readdir, stat, writeFile } from "fs/promises";
import { join } from "path";

const PHOTOS_DIR = "public/photos";
const OUTPUT = "src/data/photos.js";

async function generate() {
  const entries = {};

  let dirs;
  try {
    dirs = await readdir(PHOTOS_DIR);
  } catch {
    console.log("No photos directory found.");
    return;
  }

  for (const code of dirs.sort()) {
    const dirPath = join(PHOTOS_DIR, code);
    const dirStat = await stat(dirPath);
    if (!dirStat.isDirectory()) continue;
    if (code === "thumbs" || code === ".DS_Store") continue;

    const files = await readdir(dirPath);
    const imageFiles = files
      .filter(f => /\.(jpg|jpeg|png|webp)$/i.test(f) && f !== "thumbs")
      .sort();

    // Only include files that have actual content (non-zero size)
    const validFiles = [];
    for (const f of imageFiles) {
      const fStat = await stat(join(dirPath, f));
      if (fStat.size > 0) validFiles.push(f);
    }

    if (validFiles.length > 0) {
      entries[code] = validFiles;
    }
  }

  // Generate photos.js
  let js = `// Auto-generated by scripts/generate-photos-js.mjs
// Photo metadata per country — photos in public/photos/{CODE}/
// Thumbnails are auto-generated at build time in public/photos/{CODE}/thumbs/

export const PHOTOS = {\n`;

  for (const [code, files] of Object.entries(entries)) {
    js += `  ${code}: [\n`;
    for (const f of files) {
      js += `    { file: "${f}" },\n`;
    }
    js += `  ],\n`;
  }

  js += `};

// Helper: get photo URL
export function getPhotoUrl(code, file) {
  return \`\${import.meta.env.BASE_URL}photos/\${code}/\${file}\`;
}

// Helper: get thumbnail URL
export function getThumbUrl(code, file) {
  return \`\${import.meta.env.BASE_URL}photos/\${code}/thumbs/\${file}\`;
}

// Get countries that have photos
export function getCountriesWithPhotos() {
  return Object.keys(PHOTOS).filter(code => PHOTOS[code]?.length > 0);
}

// Get total photo count
export function getTotalPhotoCount() {
  return Object.values(PHOTOS).reduce((sum, arr) => sum + arr.length, 0);
}
`;

  await writeFile(OUTPUT, js);
  const totalPhotos = Object.values(entries).reduce((s, a) => s + a.length, 0);
  const totalCountries = Object.keys(entries).length;
  console.log(`Generated ${OUTPUT}: ${totalCountries} countries, ${totalPhotos} photos`);
}

generate();
